// Driver for `flutter drive` screenshot capture. Receives each screenshot from
// the integration test's binding.takeScreenshot(name) call and writes it to
// screenshots/<name>.png at the project root (collected by CI).
import 'dart:io';
import 'package:integration_test/integration_test_driver_extended.dart';

Future<void> main() async {
  await integrationDriver(
    onScreenshot: (String name, List<int> bytes, [Map<String, Object?>? args]) async {
      final file = File('screenshots/$name.png');
      await file.create(recursive: true);
      await file.writeAsBytes(bytes);
      return true;
    },
  );
}
